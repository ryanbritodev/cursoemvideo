# Laço de Repetição utilizando WHILE
# Quando não sabemos qual será o determinante final do nosso intervalo, usamos o WHILE
# O "WHILE" equivale a "ENQUANTO", e no exemplo do caminho da maçã, nós poderiamos fazer o uso dele caso não soubéssemos a variável de limite de repetição

while not "MAÇÃ":
    print("PASSO")
print("PEGA")

while not "MAÇÃ":
    if "CHÃO NA FRENTE":
        print("PASSO")
    if "BURACO NA FRENTE":
        print("PULA")
    if "MOEDA":
        print("PEGA")
print("PEGA")
