# Função para realizar soma de dois parâmetros (apenas)
def soma(a, b):
    print(f"A = {a} e B = {b}")
    s = a + b
    print(f"A soma entre A + B = {s}\n")


soma(1, 10)
soma(8, 9)
soma(2, 4)
soma(b=3, a=9)  # Alterar a ordem dos parâmetros



