# Interactive Help | Ajuda Interativa
# Como podemos obter ajuda para saber mais sobre comandos, funções e afins?
# Podemos utilizar a função interna help()!

# Após utilizar essa função interna, podemos passar pra ele
# qualquer comando ou biblioteca em Python que ele irá detalhar quais são
# suas especificações!

# help()
help(print)

# Exemplos:
# len
# datetime
# print

# Para sair, é só digitar quit

print(input.__doc__)

# Atributo sobre a função, podendo conter algumas informações diferentes

help(__doc__)
